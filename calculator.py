class add:
    def __init__(self, num1, num2):
        print(num1 + num2)
class subtr:
    def __init__(self, num1, num2):
        print(num1 - num2)
class multp:
    def __init__(self, num1, num2):
        print(num1 * num2)
class divs:
    def __init__(self, num1, num2):
        print(num1 / num2)
class pwr:
    def __init__(self, num1, num2):
        print(num1 ** num2)
class sqrt:
    def __init__(self, num1):
        print(num1 ** (1/2))
